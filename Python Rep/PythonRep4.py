# Ввод целого числа
num = int(input())

# Проверка условий
if num < 0:
    # Если отрицательное - преобразуем в положительное
    result = -num
elif num == 0:
    # Если равно нулю - превращаем в единицу
    result = 1
else:
    # Иначе - оставляем как есть
    result = num

# Вывод результата
print(result)
